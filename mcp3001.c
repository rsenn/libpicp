// -------------------------------------------------------------------------
uint8_t
mcp3001_str_width(const char* c) {
  uint8_t i;
  i = 0;
  while(*c) {
    ++i;
    ++c;
  }
  //  return i;
  return ((i << 1) + i) << 1; // return i*6
}

// -------------------------------------------------------------------------
void
mcp3001_send(uint8_t a, uint8_t cmd) {
  // set if data or command byte
  MCP3001_DC = 1;
  if(cmd == MCP3001_TCMD) {
    MCP3001_DC = 0;
  }
  MCP3001_NOP();
  MCP3001_CE = 0;
  // clock in data in A
  CLK_IN(a, BIT7);
  CLK_IN(a, BIT6);
  CLK_IN(a, BIT5);
  CLK_IN(a, BIT4);
  CLK_IN(a, BIT3);
  CLK_IN(a, BIT2);
  CLK_IN(a, BIT1);
  CLK_IN(a, BIT0);
  MCP3001_NOP();
  MCP3001_CE = 1;
}

// -------------------------------------------------------------------------
void
mcp3001_init(void) {
  MCP3001_TRIS();
  __delay_ms(20);
  // delay10ms(20);
  MCP3001_NOP();
  MCP3001_CLK = 0;
  MCP3001_NOP();
  MCP3001_DATA = 1;
  MCP3001_NOP();
  MCP3001_DC = 0;
  MCP3001_NOP();
  MCP3001_CE = 1;
  MCP3001_NOP();
  // reset MCP3001
  MCP3001_RESET = 0;
  __delay_ms(20);
  // delay10ms(20);
  MCP3001_RESET = 1;
#if 1
  mcp3001_send(0x21, MCP3001_TCMD); // extended commands
  mcp3001_send(0xC8, MCP3001_TCMD); // Vop (contrast)
  mcp3001_send(0x06, MCP3001_TCMD); // Temp coefficient
  mcp3001_send(0x13, MCP3001_TCMD); // MCP3001 bias mode 1:48
  mcp3001_send(0x20, MCP3001_TCMD); // MCP3001 standard, Horiz addressing
  mcp3001_send(0x0C, MCP3001_TCMD); // display control MCP3001 normal mode
// mcp3001_send(0b00001101,MCP3001_TCMD);  //display control MCP3001 invert mode
// mcp3001_send(0b00001001, MCP3001_TCMD);  //turn on all segments
#else
  mcp3001_send(0xA6, MCP3001_TCMD); // normal, 0xA7 negative
  mcp3001_send(0xA3, MCP3001_TCMD); // bias
  mcp3001_send(0xA1, MCP3001_TCMD); // addressing
  mcp3001_send(0xC0, MCP3001_TCMD); // direct, linear, normal
  mcp3001_send(0x22, MCP3001_TCMD); // V5 voltage
  mcp3001_send(0x81, MCP3001_TCMD); //(volume electric)
  mcp3001_send(0x2E, MCP3001_TCMD); // multiplier+stabilizer voltage
  mcp3001_send(0x2F, MCP3001_TCMD); // urmarire?
  mcp3001_send(0xE3, MCP3001_TCMD); // nop
  mcp3001_send(0x40, MCP3001_TCMD); // linear start address
  mcp3001_send(0xAF, MCP3001_TCMD); // lcd on, 0xAE lcd off
  mcp3001_send(0xA5, MCP3001_TCMD); // lcd all on, test?
#endif
}

// -------------------------------------------------------------------------
void
mcp3001_test(void) {
  uint16_t i;
  mcp3001_gotoxy(0, 0);
  for(i = 0; i < 504; ++i) {
    mcp3001_send(i, MCP3001_TDATA);
  }
}

// -------------------------------------------------------------------------
void
mcp3001_clear(void) {
  uint16_t i;
  mcp3001_gotoxy(0, 0);
  for(i = 0; i < 504; ++i) {
    mcp3001_send(0, MCP3001_TDATA);
  }
}

// -------------------------------------------------------------------------
void
mcp3001_gotoxy(uint8_t x, unsigned y) {
  mcp3001_send(x | 0b10000000, MCP3001_TCMD);
  mcp3001_send((y & 0b00000111) | 0b01000000, MCP3001_TCMD);
}

// -------------------------------------------------------------------------
void
mcp3001_putch(char ch) {
  uint8_t c = ch;
  if(c > 'z' || c < 32) {
    return;
  }
  mcp3001_send(mcp3001_font[c - 32][0], MCP3001_TDATA);
  mcp3001_send(mcp3001_font[c - 32][1], MCP3001_TDATA);
  mcp3001_send(mcp3001_font[c - 32][2], MCP3001_TDATA);
  mcp3001_send(mcp3001_font[c - 32][3], MCP3001_TDATA);
  mcp3001_send(mcp3001_font[c - 32][4], MCP3001_TDATA);
  mcp3001_send(0, MCP3001_TDATA);
}

