// -------------------------------------------------------------------------
void
mcp3001_battery(uint8_t chg) {
  uint8_t i;
  chg = 12 - chg;
  mcp3001_gotoxy(71, 0);
  for(i = 0; i < 13; ++i) {
    if(i >= chg) {
      mcp3001_send(0b11111110, MCP3001_TDATA);
    } else {
      mcp3001_send(bat_symbol[i], MCP3001_TDATA);
    }
  }
}

