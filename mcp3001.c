// -------------------------------------------------------------------------
void
mcp3001_clear_line(uint8_t y) {
  uint8_t k;
  mcp3001_gotoxy(0, y);
  for(k = 0; k < 83; ++k) {
    mcp3001_send(0, MCP3001_TDATA);
  }
}

// -------------------------------------------------------------------------
void
mcp3001_center_puts2(uint8_t y, char* c, uint8_t len) {
  mcp3001_gotoxy(42 - (len << 1) - len, y);
  while(len) {
    mcp3001_putch(*c);
    ++c;
    --len;
  }
}

