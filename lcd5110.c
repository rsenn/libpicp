// -------------------------------------------------------------------------
void
lcd_clear_line(uint8_t y) {
  uint8_t k;
  lcd_gotoxy(0, y);
  for(k = 0; k < 83; ++k) {
    lcd_send(0, LCD_TDATA);
  }
}

// -------------------------------------------------------------------------
void
lcd_center_puts2(uint8_t y, char* c, uint8_t len) {
  lcd_gotoxy(42 - (len << 1) - len, y);
  while(len) {
    lcd_putch(*c);
    ++c;
    --len;
  }
}

