// -------------------------------------------------------------------------
void
lcd_battery(uint8_t chg) {
  uint8_t i;
  chg = 12 - chg;
  lcd_gotoxy(71, 0);
  for(i = 0; i < 13; ++i) {
    if(i >= chg) {
      lcd_send(0b11111110, LCD_TDATA);
    } else {
      lcd_send(bat_symbol[i], LCD_TDATA);
    }
  }
}

